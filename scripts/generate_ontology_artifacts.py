# Driver
# ---------------------------------------------------------------------------

def regenerate(schema_path: Path, out_dir: Path) -> dict[str, Path]:
    """Regenerate all artifacts into ``out_dir`` and return their paths."""
    out_dir.mkdir(parents=True, exist_ok=True)
    artifacts = {
        "models.py": out_dir / "models.py",
        "schema.json": out_dir / "schema.json",
        "shapes.ttl": out_dir / "shapes.ttl",
        "cypher_constraints.cypher": out_dir / "cypher_constraints.cypher",
    }
    generate_pydantic(schema_path, artifacts["models.py"])
    generate_jsonschema(schema_path, artifacts["schema.json"])
    generate_shacl(schema_path, artifacts["shapes.ttl"])
    generate_cypher(schema_path, artifacts["cypher_constraints.cypher"])

    # Drop an __init__.py so ``app.ontology.generated`` is a proper package.
    init_path = out_dir / "__init__.py"
    init_text = (
        f"# {HEADER_TEXT}\n"
        '"""Generated LinkML artifacts (see scripts/generate_ontology_artifacts.py)."""\n'
    )
    init_path.write_text(init_text, encoding="utf-8")
    artifacts["__init__.py"] = init_path
    return artifacts


def diff_dirs(left: Path, right: Path) -> list[str]:
    """Return a unified-diff-flavored list of mismatching file names.

    Returns an empty list when the two directories are equivalent (same files,
    same contents).  This is the basis of the ``--check`` drift gate.
    """
    diffs: list[str] = []
    left_files = {p.name for p in left.iterdir() if p.is_file()}
    right_files = {p.name for p in right.iterdir() if p.is_file()}
    only_left = left_files - right_files
    only_right = right_files - left_files
    for name in sorted(only_left):
        diffs.append(f"  only in fresh generation: {name}")
    for name in sorted(only_right):
        diffs.append(f"  only in committed tree:   {name}")
    common = sorted(left_files & right_files)
    for name in common:
        a, b = left / name, right / name
        if not filecmp.cmp(a, b, shallow=False):
            diffs.append(f"  drift: {name}")
            sample = list(difflib.unified_diff(
                b.read_text(encoding="utf-8").splitlines(),
                a.read_text(encoding="utf-8").splitlines(),
                fromfile=str(b),
                tofile=str(a),
                n=2,
                lineterm="",
            ))[:30]
            diffs.extend(f"    {line}" for line in sample)
    return diffs


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Regenerate LinkML artifacts.")
    parser.add_argument(
        "--schema",
        type=Path,
        default=DEFAULT_SCHEMA,
        help="Path to the LinkML source schema.",
    )
    parser.add_argument(
        "--out-dir",
        type=Path,
        default=DEFAULT_OUTDIR,
        help="Directory to write generated artifacts into.",
    )
    parser.add_argument(
        "--check",
        action="store_true",
        help=(
            "Regenerate into a temp dir and diff against the committed "
            "artifacts.  Exits non-zero on drift."
        ),
    )
    args = parser.parse_args(argv)

    if not args.schema.exists():
        print(f"error: schema not found at {args.schema}", file=sys.stderr)
        return 2

    if args.check:
        with tempfile.TemporaryDirectory() as tmp:
            tmp_dir = Path(tmp)
            regenerate(args.schema, tmp_dir)
            if not args.out_dir.exists():
                print(
                    f"error: committed output dir {args.out_dir} is missing; "
                    "run `make generate` first.",
                    file=sys.stderr,
                )
                return 1
            diffs = diff_dirs(tmp_dir, args.out_dir)
            if diffs:
                print("Ontology artifacts are stale; please run `make generate`:")
                for line in diffs:
                    print(line)
                return 1
            print("Ontology artifacts are up to date.")
            return 0

    # Normal regenerate mode.
    artifacts = regenerate(args.schema, args.out_dir)
    for name, path in artifacts.items():
        try:
            display = path.relative_to(PROJECT_ROOT)
        except ValueError:
            display = path
        print(f"wrote {name} -> {display}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
